package com.entrobus.credit.vo.product;

public class InterestVo {

    private Integer term;

    private String interestFirst;

    private String averageCapitalInterest;

    public Integer getTerm() {
        return term;
    }

    public void setTerm(Integer term) {
        this.term = term;
    }

    public String getInterestFirst() {
        return interestFirst;
    }

    public void setInterestFirst(String interestFirst) {
        this.interestFirst = interestFirst;
    }

    public String getAverageCapitalInterest() {
        return averageCapitalInterest;
    }

    public void setAverageCapitalInterest(String averageCapitalInterest) {
        this.averageCapitalInterest = averageCapitalInterest;
    }

}
